package com.example.hyunm.sittingcafe;

public class CafeData {
    private String cafe_name;
    private String cafe_per;
    private String cafe_people;
    private String cafe_table;
    private String cafe_chair;

    public String getCafe_name() {
        return cafe_name;
    }

    public void setCafe_name(String cafe_name) {
        this.cafe_name = cafe_name;
    }

    public String getCafe_per() {
        return cafe_per;
    }

    public void setCafe_per(String cafe_per) {
        this.cafe_per = cafe_per;
    }

    public String getCafe_people() {
        return cafe_people;
    }

    public void setCafe_people(String cafe_people) {
        this.cafe_people = cafe_people;
    }

    public String getCafe_table() {
        return cafe_table;
    }

    public void setCafe_table(String cafe_table) {
        this.cafe_table = cafe_table;
    }

    public String getCafe_chair() {
        return cafe_chair;
    }

    public void setCafe_chair(String cafe_chair) {
        this.cafe_chair = cafe_chair;
    }

}